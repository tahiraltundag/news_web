export const getResultLabel = (match, teamId) => {
  const isHome = match.home === teams[teamId]?.name || match.isHome;
  const won = isHome ? match.homeScore > match.awayScore : match.awayScore > match.homeScore;
  const draw = match.homeScore === match.awayScore;
  if (draw) return 'B';
  return won ? 'G' : 'M';
};
